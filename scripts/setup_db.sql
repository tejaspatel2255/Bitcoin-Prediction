-- SQL script to setup the database schema in Supabase / PostgreSQL

-- 1. Create table for historical price and technical indicators data
CREATE TABLE IF NOT EXISTS historical_prices (
    id SERIAL PRIMARY KEY,
    date DATE UNIQUE NOT NULL,
    close_price NUMERIC(12, 2) NOT NULL,
    open_price NUMERIC(12, 2) NOT NULL,
    high_price NUMERIC(12, 2) NOT NULL,
    low_price NUMERIC(12, 2) NOT NULL,
    volume NUMERIC(20, 2) NOT NULL,
    rsi_14 NUMERIC(6, 2),
    macd NUMERIC(10, 4),
    macd_signal NUMERIC(10, 4),
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 2. Create table for machine learning predictions
CREATE TABLE IF NOT EXISTS predictions (
    id SERIAL PRIMARY KEY,
    prediction_date DATE UNIQUE NOT NULL,  -- Date the prediction is targeting
    run_date TIMESTAMP WITH TIME ZONE DEFAULT NOW(), -- When prediction was generated
    prophet_price NUMERIC(12, 2) NOT NULL,
    lstm_price NUMERIC(12, 2) NOT NULL,
    sklearn_price NUMERIC(12, 2) NOT NULL,
    ensemble_price NUMERIC(12, 2) NOT NULL,
    predicted_direction VARCHAR(4) NOT NULL, -- 'UP' or 'DOWN'
    trend_7day VARCHAR(50) NOT NULL,          -- 'BULLISH', 'BEARISH', or 'NEUTRAL'
    actual_price NUMERIC(12, 2),              -- Filled in later when data is available
    prediction_error NUMERIC(6, 2)            -- Absolute percentage error
);

-- 3. Create table for AI insights generated by Gemini Flash
CREATE TABLE IF NOT EXISTS ai_insights (
    id SERIAL PRIMARY KEY,
    prediction_date DATE UNIQUE REFERENCES predictions(prediction_date) ON DELETE CASCADE,
    insight_text TEXT NOT NULL,
    sentiment_score VARCHAR(20) NOT NULL,    -- 'BULLISH', 'BEARISH', 'NEUTRAL'
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- Create indexes for performance optimization
CREATE INDEX IF NOT EXISTS idx_historical_prices_date ON historical_prices(date);
CREATE INDEX IF NOT EXISTS idx_predictions_date ON predictions(prediction_date);
