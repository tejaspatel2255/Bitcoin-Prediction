import os
import joblib
import numpy as np
import pandas as pd
from sklearn.ensemble import RandomForestRegressor, RandomForestClassifier
from sklearn.metrics import (
    mean_absolute_error, mean_squared_error, r2_score,
    accuracy_score, mean_absolute_percentage_error
)
from app.core.logger import get_logger

logger = get_logger("rf_model")

# Default paths for saved model artifacts
MODEL_DIR = "data/saved_models"
REGRESSOR_PATH  = os.path.join(MODEL_DIR, "rf_regressor.joblib")
CLASSIFIER_PATH = os.path.join(MODEL_DIR, "rf_classifier.joblib")
FEATURES_PATH   = os.path.join(MODEL_DIR, "rf_features.joblib")
os.makedirs(MODEL_DIR, exist_ok=True)

# All technical indicator columns generated by data_service.add_technical_indicators()
# plus lag features for lag windows. These must exactly match training features.
INDICATOR_FEATURES = [
    "open", "high", "low", "volume",
    "sma_7", "sma_21", "sma_50",
    "ema_12", "ema_26",
    "rsi_14", "macd", "macd_signal",
    "bb_mid", "bb_upper", "bb_lower",
    "daily_return", "log_return",
    "volume_sma", "volatility"
]


def _build_feature_matrix(df: pd.DataFrame, lag_windows: list = [1, 3, 7]) -> pd.DataFrame:
    """
    Build a feature matrix by combining live technical indicators with lag features.
    
    Args:
        df:          DataFrame with technical indicators computed by data_service.
        lag_windows: List of lag days to generate lag features for.
    
    Returns:
        DataFrame with original features + lag features for close, volume, and rsi.
    """
    df = df.copy().sort_values("date").reset_index(drop=True)

    # Select only the base indicator features that are available
    available_base = [c for c in INDICATOR_FEATURES if c in df.columns]
    result = df[["date", "close"] + available_base].copy()

    # Generate lag features for the most predictive signals
    for lag in lag_windows:
        result[f"close_lag_{lag}"] = result["close"].shift(lag)
        result[f"volume_lag_{lag}"] = df["volume"].shift(lag) if "volume" in df.columns else 0
        result[f"rsi_lag_{lag}"] = df["rsi_14"].shift(lag) if "rsi_14" in df.columns else 0

    # Target: next-day close (shift close by -1)
    result["target_price"] = result["close"].shift(-1)
    # Target: direction (1 = UP, 0 = DOWN compared to current close)
    result["target_direction"] = (result["target_price"] > result["close"]).astype(int)

    # Drop rows with NaN (from shifts at start and end)
    result = result.dropna().reset_index(drop=True)
    return result


def _get_feature_cols(df: pd.DataFrame) -> list:
    """
    Dynamically compute the feature column list from the processed DataFrame,
    excluding date and target columns.
    """
    exclude = {"date", "close", "target_price", "target_direction"}
    return [c for c in df.columns if c not in exclude]


def train_regressor(df: pd.DataFrame, lag_windows: list = [1, 3, 7]) -> RandomForestRegressor:
    """
    Train a Random Forest Regressor to predict next-day Bitcoin close price.

    Args:
        df:          DataFrame with technical indicators.
        lag_windows: Lag feature windows to engineer.

    Returns:
        Fitted RandomForestRegressor model.
    """
    logger.info("Training Random Forest Regressor for next-day price prediction...")
    ml_df = _build_feature_matrix(df, lag_windows)

    if len(ml_df) < 100:
        raise ValueError(f"Insufficient data for RF training: {len(ml_df)} rows (need >= 100).")

    feature_cols = _get_feature_cols(ml_df)

    X = ml_df[feature_cols].values
    y = ml_df["target_price"].values

    # Sequential 80/20 time-series split (no shuffling to avoid data leakage)
    split = int(len(X) * 0.8)
    X_train, y_train = X[:split], y[:split]

    model = RandomForestRegressor(
        n_estimators=200,
        max_depth=10,
        min_samples_split=5,
        min_samples_leaf=2,
        random_state=42,
        n_jobs=-1
    )
    model.fit(X_train, y_train)
    logger.info(f"RF Regressor trained on {len(X_train)} samples with {len(feature_cols)} features.")

    # Save model and features list together
    save_regressor(model, feature_cols)
    return model


def train_classifier(df: pd.DataFrame, lag_windows: list = [1, 3, 7]) -> RandomForestClassifier:
    """
    Train a Random Forest Classifier to predict whether Bitcoin price will go UP or DOWN.

    Args:
        df:          DataFrame with technical indicators.
        lag_windows: Lag feature windows to engineer.

    Returns:
        Fitted RandomForestClassifier model.
    """
    logger.info("Training Random Forest Classifier for next-day direction prediction...")
    ml_df = _build_feature_matrix(df, lag_windows)

    if len(ml_df) < 100:
        raise ValueError(f"Insufficient data for RF classification training: {len(ml_df)} rows.")

    feature_cols = _get_feature_cols(ml_df)

    X = ml_df[feature_cols].values
    y = ml_df["target_direction"].values

    split = int(len(X) * 0.8)
    X_train, y_train = X[:split], y[:split]

    model = RandomForestClassifier(
        n_estimators=200,
        max_depth=8,
        min_samples_split=5,
        min_samples_leaf=2,
        random_state=42,
        n_jobs=-1
    )
    model.fit(X_train, y_train)
    logger.info(f"RF Classifier trained on {len(X_train)} samples.")

    save_classifier(model)
    return model


def predict_next_day(
    reg_model: RandomForestRegressor,
    clf_model: RandomForestClassifier,
    latest_row: pd.DataFrame,
    feature_cols: list
) -> dict:
    """
    Predict tomorrow's Bitcoin price and direction using the latest single-row data.

    Args:
        reg_model:    Trained RF price regressor.
        clf_model:    Trained RF direction classifier.
        latest_row:   Single-row DataFrame with all indicator features.
        feature_cols: List of feature columns the model was trained on.

    Returns:
        Dict with keys: predicted_price (float), predicted_direction (int 0|1),
                        direction_label (str 'UP'|'DOWN').
    """
    available_cols = [c for c in feature_cols if c in latest_row.columns]
    missing_cols   = [c for c in feature_cols if c not in latest_row.columns]

    if missing_cols:
        # Fill missing features (lag columns may be absent for very short inputs)
        logger.warning(f"Filling {len(missing_cols)} missing feature columns with 0.")
        for c in missing_cols:
            latest_row[c] = 0.0

    X = latest_row[feature_cols].values[-1:] # Ensure shape (1, n_features)

    price_pred    = float(reg_model.predict(X)[0])
    dir_pred      = int(clf_model.predict(X)[0])
    dir_label     = "UP" if dir_pred == 1 else "DOWN"

    logger.info(f"RF Prediction — Price: ${price_pred:,.2f}, Direction: {dir_label}")
    return {
        "predicted_price": price_pred,
        "predicted_direction": dir_pred,
        "direction_label": dir_label
    }


def evaluate(
    reg_model: RandomForestRegressor,
    clf_model: RandomForestClassifier,
    df: pd.DataFrame,
    lag_windows: list = [1, 3, 7]
) -> dict:
    """
    Evaluate both the regressor and classifier on the held-out test set.

    Args:
        reg_model:    Trained RF regressor.
        clf_model:    Trained RF classifier.
        df:           Full historical DataFrame with technical indicators.
        lag_windows:  Lag windows to match training.

    Returns:
        Dict with MAE, RMSE, R2, MAPE (regression), Accuracy (classification).
    """
    ml_df = _build_feature_matrix(df, lag_windows)
    feature_cols = _get_feature_cols(ml_df)

    X = ml_df[feature_cols].values
    y_reg = ml_df["target_price"].values
    y_clf = ml_df["target_direction"].values

    split = int(len(X) * 0.8)
    X_test, y_reg_test, y_clf_test = X[split:], y_reg[split:], y_clf[split:]

    # Regression metrics
    y_reg_pred = reg_model.predict(X_test)
    mae      = float(mean_absolute_error(y_reg_test, y_reg_pred))
    rmse     = float(np.sqrt(mean_squared_error(y_reg_test, y_reg_pred)))
    r2       = float(r2_score(y_reg_test, y_reg_pred))
    mape     = float(mean_absolute_percentage_error(y_reg_test, y_reg_pred) * 100)

    # Classification metrics
    y_clf_pred = clf_model.predict(X_test)
    accuracy = float(accuracy_score(y_clf_test, y_clf_pred) * 100)

    metrics = {
        "mae": round(mae, 2), "rmse": round(rmse, 2),
        "r2": round(r2, 4),   "mape": round(mape, 4),
        "accuracy": round(accuracy, 2)
    }
    logger.info(f"RF Evaluation — MAE: {mae:.2f}, RMSE: {rmse:.2f}, R2: {r2:.4f}, Accuracy: {accuracy:.2f}%")
    return metrics


def save_regressor(model: RandomForestRegressor, feature_cols: list, path: str = REGRESSOR_PATH) -> None:
    """Persist the RF regressor and its feature list to disk using joblib."""
    os.makedirs(os.path.dirname(path), exist_ok=True)
    joblib.dump(model, path)
    joblib.dump(feature_cols, FEATURES_PATH)
    logger.info(f"RF Regressor saved to {path}. Features saved to {FEATURES_PATH}")


def load_regressor(path: str = REGRESSOR_PATH) -> tuple:
    """
    Load the RF regressor and its associated feature list from disk.
    
    Returns:
        Tuple of (RandomForestRegressor, list[str]) — model and feature columns.
    """
    if not os.path.exists(path) or not os.path.exists(FEATURES_PATH):
        raise FileNotFoundError(f"RF Regressor not found at {path}. Please train first.")
    model        = joblib.load(path)
    feature_cols = joblib.load(FEATURES_PATH)
    logger.info(f"RF Regressor loaded from {path}")
    return model, feature_cols


def save_classifier(model: RandomForestClassifier, path: str = CLASSIFIER_PATH) -> None:
    """Persist the RF classifier to disk using joblib."""
    os.makedirs(os.path.dirname(path), exist_ok=True)
    joblib.dump(model, path)
    logger.info(f"RF Classifier saved to {path}")


def load_classifier(path: str = CLASSIFIER_PATH) -> RandomForestClassifier:
    """Load the RF classifier from disk."""
    if not os.path.exists(path):
        raise FileNotFoundError(f"RF Classifier not found at {path}. Please train first.")
    model = joblib.load(path)
    logger.info(f"RF Classifier loaded from {path}")
    return model
